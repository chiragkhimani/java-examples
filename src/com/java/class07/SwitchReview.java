package com.java.class07;

public class SwitchReview {
	public static void main(String[] args) {

		int choice = 5;

		switch (choice) {
		
		case 5:
			System.out.println("Five");

		case 1:
			System.out.println("One");

		case 2:
			System.out.println("Two");

		case 3:
			System.out.println("Threee");

		case 4:
			System.out.println("Four");

		}
	}
}
