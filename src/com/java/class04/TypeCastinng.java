package com.java.class04;

public class TypeCastinng {
	public static void main(String args[]) {
		double d = 10;
		
		int i = (int)10.923; // Type casting
		
		System.out.println(10 / 3);
		
		int j = 'Z';
		
		System.out.println(j);
		
		String str = "123";
		
		int k = str;
		
		boolean b = (boolean)"true";
		
		System.out.println(k);
		
	}
}
